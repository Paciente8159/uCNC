#define mcu_get_analog(diopin)                                   \
	{                                                              \
		while (ADC->STATUS.bit.SYNCBUSY)                             \
			ADC->INTFLAG.reg = ADC_INTFLAG_RESRDY;                     \
		;                                                            \
		while (ADC->STATUS.bit.SYNCBUSY)                             \
			;                                                          \
		ADC->INPUTCTRL.bit.MUXPOS = (__indirect__(diopin, CHANNEL)); \
		while (ADC->STATUS.bit.SYNCBUSY)                             \
			;                                                          \
		ADC->SWTRIG.bit.START = 1;                                   \
		while (!(ADC->INTFLAG.bit.RESRDY))                           \
			;                                                          \
		ADC->RESULT.reg;                                             \
	}

